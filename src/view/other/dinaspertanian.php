          <h5>SEJARAH RINGKAS DINAS PERTANIAN</h5>
          <br>
          <h6>SEBELUM KEMERDEKAAN RI.</h6>
          <p>Pemerintahan Hindia Belanda mencoba menyampaikan hasil percobaan kepada masyarakat 
          tani untuk meningkatan produk-si pertanian pada tahun 1870.</p>
          <p>Kegiatan Peringatan Dinas Pertanian Provinsi Sumatera Utara melalui penyuluhan pertanian 
          berjalan terasa sangat lambat. Dalam menggugah Swadaya Pertanian begitu lamban, 
          disebabkan terbatasnya pendidikan bagi masyarakat. Usaha dalam peningkatan produksi 
          Pertanian Tanaman Pangan menjadi lebih nyata setelah didirikan Departemen Vanland Bouw 
          (Depar-temen Pertanian) pada tahun 1905: Jawatan Pertanian Rakyat ) dan tahun 1910 
          sebagai salah satu Departemen yang mempunyai kegiatan Melaksanakan Penyuluhan 
          Pertanian, memberikan saran-saran dalam bidang Pertanian dan pemberian tanah kepada 
          perusahaanperusahaan bidang Pertanian. Selain itu Dinas Pertanian juga mengadakan 
          penelitian tentang ekonomi masyarakat dan membuat laporan keadaan Pertanian termasuk 
          statistic.</p>
          <p>Pembangunan Balai Pendidikan Masyarakat Desa (BPMD) dan Pembangunan objek-objek 
          pencegahan serta Pembangunan Percobaan Perusahaan Tanah Kering (PTTK).</p>
          <br>
          <h6>SETELAH KEMERDEKAAN R.I</h6>
          <p>Rencana bagi Dinas Pertanian Provinsi Sumatera Utara di Medan setelah Kemerdekaan 
          dimulai dengan adanya “Plan Kasimo” yang merupakan rencana proklamasi tahun 1915-
          1950, namun rencana tersebut tidak dapat dilaksana-kan sepenuhnya dikarenakan oleh 
          gejolak revolusi pada waktu itu.</p>
          <p>Program pembangunan rakyat yang termasuk ke dalam Rencana Kesejah-teraan Istimewah 
          (RKI) meliputi: Pembangunan balai-balai benih, Perbaikan dan perluasan pengairan lahan 
          pedesaan.</p>
          <p>Dinas Jawatan Pertanian Rakyat Propinsi Sumatera Utara pada tahun 1915-1940 masih 
          bernama DINAS JAWATAN PERTANIAN RAKYAT kemudian diganti, dicabut dan 
          diubah berdasarkan Peraturan Daerah Propinsi Sumatera Utara No. 5 Tahun 1981 s/d Tahun 
          2001 namanya menjadi Dinas Pertanian Tanaman Pangan dan Hortikultura yang beralamat di 
          Jalan Diponegoro No. 11 A Medan.</p>
          <p>Dinas Pertanian Tanaman Pangan pada Tahun 1981 berpindah ke Jalan Dr. A.H. Nasution 
          No. 6 Gedung Johor Medan, dengan keluarnya UU No. 22 Tahun 1999 tentang Otonomi 
          Daerah, maka Dinas Pertanian Tanaman Pangan dan Hortikultura dirubah menjadi DINAS 
          PERTANIAN PROVINSI SUMATERA UTARA berdasarkan PERDA No. 3 Tahun 2001.</p>
          <p>Gedung Dinas Pertanian Provinsi Sumatera Utara disesuaikan berdirinya pada tahun 1981 
          yang beralamat di Jalan Jenderal Besar Dr. Abdul Harris Nasution No. 6 Gedung Johor 
          Medan, yang dibangun pada tahun 1950.</p>
          <p>Pada tahun 2008 bertepatan pada tanggal 24 juni pembentukan kabupaten labuhanbatu 
          selatan yang pada masa itu dibawah kepemimpinan presiden Susilo Bambang Yudhoyono 
          dan sejalan dengan pemekaran tersebut terbentuklah prangkat-prangkatan kedinasan yang 
          dimana untuk membantu jalannya birokrasi pemerintahan salahsatu nya ialah dinas pertanian 
          dan perikanan ketahanan pangan kabupaten labuhanbatu selatan.</p>
          <p>Tugas dinas pertanian ialah membantu Bupati melaksanakan urusan pemerintahan menjadi 
          kewenangan Kabupaten Labuhanbatu Seltan di bidang pertanian dan pangan serta tugas 
          pembantuan.</p>
          <p>Dalam Bidang Pertanian, ada berbagai macam pelayanan bagi petani dan peternak. Seperti 
          pada Balai Penyuluhan Pertanian, biasanya ada pelayanan penyediaan bibit tanaman, 
          pelayanan penyaluran benih, pelayanan bimbingan teknis, pelayanan pembinaan 
          kelembagaan bagi petani/kelompok tani/gapoktan, pelayanan jasa konsultasi oleh penyuluh, 
          pelayanan armada mesin panen, pengendali hama, dan armada mesin tanam, pelayanan 
          penyediaan pupuk, penyediaan data dan informasi pertanian. Sedangkan bagi petani-peternak 
          dibentuk Pusat Kesehatan Hewan (Puskeswan) ada berbagai produk pelayanan yang sering 
          dilakukan sepreti pemberian vaksinasi, pemeriksaan kebuntingan dan gangguan reproduksi, 
          inseminasi buatan, disinfeksi, pengambilan sampel, pelayanan pemeriksaan hewan, 
          pelayanan terpadu kesehatan hewan, pelayanan informasi, konsultasi dan edukasi peternakan 
          dan bahkan pelayanan visum et repertum.</p>        </div>
