          <h3>Tanaman Kangkung Dan Kacang Panjang</h3>
          <br>
          <h4>Perawatan</h4>
          <h6>Perawatan Pada Tanaman Kangkung Dan Kacang Panjang</h6>
          <br>
          <h5>Kacang Panjang</h5>
          <p>Pemeliharaan atupun Perawatan ialah perlakuan terhadap tanaman dan lingkungan nya agar 
          tanaman tumbuh sehat dan normal. Perawatan tanaman meliputi :</p>
          <br>
          <h6>• Penyulaman</h6>
          <p>Penyulaman adalah kegiatan mengganti tanaman mati,rusak atau dapat dikatakan tidak sehat 
          dengan menggunakan bibit baru dan penyulaman juga dapat di artikan pemindahan media 
          tanam contohnya dari media tanam polibag kepada media tanam bedengan ataupun lahan 
          pertanian. pada penyulaman tanaman kacang panjang penyulaman dilakukan paling lambat 
          seminggu setelah tanam dari benih yang telah disiapkan sebelumnya dalam polybag.</p>
          <br>
          <h6>• Penyiraman</h6>
          <p>Penyiraman tanaman merupakan suatu kegiatan yang perlu diperhatikan dalam melakukan 
          pemeliharaan tanaman, dikarenakan tanaman memerlukan air yang cukup untuk melakukan 
          fotosintesis dalam memperoleh kebutuhannya untuk tumbuh dan berkembang.</p>
          <br>
          <h6>• Penyiangan</h6>
          <p>Penyiangan dilakukan dua kali yaitu setelah tanaman berumur 3 minggu dan 6 minggu. 
          Penyiangan ini dilakukan dengan cara mencabut rumput liar yang tumbuh di sekitar tanaman 
          dan sekaligus melakukan pendangiran.</p>
          <br>
          <h6>• Pemangkasan/Perempelan</h6>
          <p>Tanaman kacang panjang yang terlalu rimbun perlu diadakan pemangkasan daun maupun 
          ujung batang karena jika terlalu rimbun dapat menghambat pertumbuhan bunga dan akan 
          berpengaruh pada produksi tanaman kacang panjang.</p>
          <br>
          <h6>• Pemupukan</h6>
          <p>Pemupukan ialah penambahan satu atau beberapa hara tanaman yang tersedia atau dapat 
          tersedia kedalam tanah/tanaman agar dapat mempertahankan kesuburan tanah ditujukan 
          untuk mencapai hasil/produksi yang tinggi.</p>
          <br>
          <h6>Pupuk Dasar</h6>
          <p>Kacang panjang tipe merambat: Urea 150 kg + TSP 100 kg + 100 kg/ha.
          Kacang panjang tipe tegak: Urea 22,5 kg + TSP 45 kg + KCl 45 kg/ha.
          Kacang hibrida: 85 kg Urea + 310-420 kg TSP + 210 kg KCl/ha.
          Pupuk diberikan di dalam lubang pupuk yang terletak di kiri-kanan lubang tanam. Jumlah 
          pupuk yang diberikan untuk satu tanaman tergantung dari jarak tanam.</p>
          <br>
          <h6>Pupuk Susulan</h6>
          <p>Pupuk susulan tanaman kacang panjang tipe merambat, diberikan 4 minggu setelah tanam, 
          pupuk berupa urea 150 kg/ha. Sedangkan pupuk susulan untuk kacang panjang tipe tegak 
          diberikan 4 minggu setelah tanam, pupuk berupa urea 85 kg/ha.</p>
          <br>
          <h6>• Pemeliharaan tanaman</h6>
          <p>Pemeliharaan tanaman meliputi pengendalian gulma, pemupukan dan pengendalian hama dan 
          penyakit. Apabila terdapat tanaman yang tumbuh tidak normal dapat diganti dengan tanaman 
          yang sehat pada 7-10 HST. Pengendalian gulma dilakukan saat tanaman berusia 10 HST 
          dengan penyemprotan herbisida, saat tanaman belum berbunga dan belum keluar akar 
          gantung (umur 30 HST) serta setelah berbunga (umur 45 HST).</p>
          <br>
          <h6>• Pengendalian Hama Dan Penyakit</h6>
          <p>Pengendalian hama dan penyakit dilakukan dengan prinsip pengendalian hama terpadu, yaitu 
          : Penggunaan varietas unggul, pengaturan jadwal tanam dan pestisida nabati.</p>
          <br>
          <p>Beberapa Hama yang sering menyerbu tanaman Kangkung yaitu :</p>
          <br>
          <h6>1.Lalat Kacang(Ophiomya Phaseoli Tryon)</h6>
          <p>• Gejala serangan : Terdapat bintik-bintik putih sekitar tulang daun, pertumbuhan 
          tanaman yang terserang terhambat dan daun berwarna kekuningan, pangkal batang 
          terjadi perakaran sekunder dan membengkak</p>
          <p>• Penyebab : Lalat kacang atau nama latinnya Ophiomya phaseoli Tryon</p>
          <p>• Pengendalian : Pergiliran tanaman yang bukan berasal dari family kacang-kacangan, 
          penyemprotan dengan insektisida</p>
          <br>
          <h6>2. Kutu Daun</h6>
          <p>• Gejala Serangan : Pertumbuhan terlambat karena hama mengisap cairan sel tanaman 
          dan penurunan hasil panen. Kutu bergerombol di pucuk tanaman dan berperan sebagai 
          vector virus.</p>
          <p>• Penyebab : Kutu daun Aphis Cracivora Koch</p>
          <p>• Pengendalian : Rotasi tanaman dengan yang bukan dari family kacang-kacangan dan 
          penyemprotan.</p>
          <br>
          <h6>3. Ulat Gereyak</h6>
          <p>• Gejala Serangan : Daun berlubang dengan ukuran tidak tentu, serangan berat terjadi 
          di musim kemarau juga menyerang polong</p>
          <p>• Penyebab : Spodoptera Litura F</p>
          <p>• Pengendalian : Dengan kultur teknis, rotasi tanaman, penanaman serempak dan 
          penyemprotan</p>
          <br>
          <h5>Beberapa Penyakit Pada Tanaman Kangkung Yaitu:</h5>
          <br>
          <h6>1. Penyakit Mozaik</h6>
          <p>• Gejala Serangan : Pada daun-daun muda terdapat gambaran mosaic yang warnanya 
          tidak beraturan. Penyakit ini ditularkan oleh vector kutu daun</p>
          <p>• Penyebab : Virus Cowpea Aphid Borne Virus</p>
          <p>• Pengendalian : Menggunakan benih sehat dan bebas virus, semprot vector kutu daun, 
          tanaman yang terserang dicabut dan dimusnahkan.</p>
          <br>
          <h6>2. Penyakit Sapu</h6>
          <p>• Gejala Serangan : Pertumbuhan tanaman terhambat, ruas-ruas (buku-buku) batang 
          sangat pendek, tunas ketiak memendek dan membentuk “sapu”. Penyakit ditularkan 
          oleh vector kutu daun.</p>
          <p>• Penyebab : Virus Cowpea Witches-broom Virus/Cowpea Stunt Virus</p>
          <p>• Pengendalian : Menggunakan benih sehat dan bebas virus, penyemprotan terhadap vector 
          kutu daun, tanaman yang terserang dicabut dan dimusnahkan.</p>
          <br>
          <h6>3. Layu Bakteri</h6>
          <p>• Gejala Serangan : Tanaman mendadak layu dan serangan berat menyebabkan 
          tanaman mati</p>
          <p>• Penyebab : Pseudomonas solanacearum</p>
          <p>• Pengendalian : Rotasi tanaman, perbaikan drainase, tanaman yang mati dicabut dan 
          dimusnahkan.</p>
          <br>
          <h5>Kangkung Darat</h5>
          <p>Kangkung darat (Ipomea reptans Poir) merupakan salah satu jenis sayuran yang sangat 
          populer bagi rakyat Indonesia dan digemari oleh semua lapisan masyarakat, karena rasanya 
          yang gurih. Tanaman kangkung termasuk kelompok tanaman sayuran semusim, berumur 
          pendek dan tidak memerlukan areal yang luas untuk membudidayakannya, sehingga 
          memungkinkan untuk dibudidayakan pada daerah perkotaan yang umumnya mempunyai 
          lahan pekarangan terbatas. Selain rasanya yang gurih, gizi yang terdapat pada sayuran 
          kangkung cukup tinggi, seperti vitamin A, B dan C serta berbagai mineral terutama zat besi 
          yang berguna bagi pertumbuhan badan dan kesehatan (Haryoto, 2009).</p>
          <br>
          <h6>1. Pemeliharaan Dan Perawatan Kangkung Darat</h6>
          <p>Sebaiknya lakukanlah penyiraman setiap pagi dan sore penyiraman juga bisa di lakukan 
          menurut cuaca,dan musim.kalo budidaya tanaman kangkung darat pada musim kemarau 
          sebaiknya di lakukan penyiraman setiap hari. Dan apabila pada musim penghujan lakukanlan 
          fermentasi dan selalu mengecek kondisi lahan,kalo telalu banyak air yang mengedap akan 
          mengakibatkan tanaman menjadi daunya layu,menguning dan mengakibatkan 
          kematian.Dalam umur tanaman kangkung darat pada Usia setengah bulan pasti sudah banyak 
          hamma/mamalia.</p>
          <br>
          <p>Jenis mamalia yang menyerang Tanaman kangkung darat adalah :</p>
          <p>1. Ulat</p>
          <p>2. Serangga</p>
          <p>3. Belalang yang memakan dedaunan</p>
          <br>
          <p>Cara mengusir semua mamalia itu cukup lakukanlah penyemprotan dengan obat pembunuh 
          hamma seperti Sevin. Untuk memberantas ulat daun ini menggunakan Insektisida Diazinon 
          60 EC, dengan Dosis sebesar 2 cc per liter air lalu semprotkan pada tanaman kangkung darat. 
          Pada waktu membasmi hama, sebaiknya lahan dikeringkan terlebih dahulu selama 4-5 hari 
          sebelum penyemprotan hal ini untuk mencegah obat-obat yang kita semprotkan tidak pergi 
          dari lahan yang di semprot,bila lahan yang akan di semprot ada airnya maka secara otomatis 
          obat yang kita berikan akan hanyut bersama air yang ada,dan penyemprotan ini akan sia-sia 
          saja,jadi sebaiknya lahan dikeringkan terlebih dahulu.</p>
          <br>
          <h6>2. Proses Pemupukan</h6>
          <p>Setelah proses pengolahan lahan selesai berikanlah pupuk kandang sebelum biji atau tunas di 
          tanam dengan jarak 4 hari,pengalaman saya dalam meberi pupuk kandang pada lahan 8×12 
          saya beri 4 kantong dengan berat 50,kg,sbenernya tanaman kangkung darat cukup di bilang 
          sangat kebal terhadap cuaca dan kondisi tanah yang lembab.</p>
          <br>
          <h6>3. Penjarangan Dan Penyulaman</h6>
          <p>Bila tanaman kangkung darat terlalu lebat maka akan sangat keliatan tidak rajin,jadi dalam 
          menanam tanaman kangkung darat biar menghasilkan hasil yang bagus maka diperlukan 
          penjarangan. Apabila tanaman banyak yang mati, Sebaiknya segeralah dilakukan penyulaman 
          dan diganti dengan bibit yang telah disiapkan sebelumnya begitupula sebaliknya,apabila di 
          dalam satau rumpun tanaman kebanyakan maka segeralah lakukan pemisahan minimal dalam 
          satu rumpun ada 3-4 batang kangkung saja.</p>
          <br>
          <h6>4. Penyiangan</h6>
          <p>Penyiangan dilakukan jika di dalam budidaya kangkung darat terdapat rumput liar Atau 
          gulma. Penyiangan ini dilakukan setiap 2 minggu sekali.Rumput liar akan sangat 
          mengganggu vermentasi pertumbuhan bibit kangkung darat. Maka dari itu harus di lakukan 
          pembersihan dengan cara manual yaitu dengan di cabut-cabut rumput liarnya dengan tangan 
          sampai tak tersisa sedikitpun.</p>
