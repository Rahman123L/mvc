          <h3>Tanaman Kangkung Dan Kacang Panjang</h3>
          <h4>Penanaman</h4>
          <h6>Penanaman pada tanaman kangkung Dan kacang panjang</h6>
          <h5>Kacang Panjang</h5>
          <p>Budidaya kangkung itu sendiri bisa menjadi prospek bisnis yang menjanjikan karena 
          kebutuhan masyarakat dengan sayuran yang satu ini sangat meningkat apalagi jika diolah 
          menjadi masakan yang gurih dan enak. Nah, kali ini kami akan memberikan informasi 
          tentang cara budidaya tanaan kangkung yang baik dan benar. Pilih Bibit yang Berkualitas 
          Karena kita ingin hasil yang maksimal, maka usahakan untuk memiliki bibit yang bagus dan 
          berkualitas unggul. Bibit yang bagus pastiakan menghasilkan hasil panen yang baik pula, 
          adapun ciri-ciri bibit bagus bagus pada awal pertumbuhan kangkung ditandai dengan tumbuh 
          tegak dan daya tumbuhnya hanya perlu 100%. Dalam melakukan pembibitan kami 
          memberikan informasi ada 2 cara yang bisa dilakukan yaitu dengan teknik stek yaitu batang 
          ditancapkan di tanah dengan kondisi bibit yang besar dan segar atau menggunakan benih 
          dengan kondisi yang bagus dan kering.</p>
          <p>Adapun beberapa hal yang perlu diperhatikan dalam penanaman kangkung sebagai berikut:</p>
          <h5>1. Persiapan Lahan</h5>
          <p>Sebaiknya menanam kangkung darat cabut lakukan di area persawahan biar terkena sinar 
          matahari yang cukup dan usahakan jangan terlalu jauh dari perairan,lahan yang harus di 
          persiapkan untuk budidaya kangkung yaitu:</p>
          <h6>• Lahan harus di cangkul terlebih dahulu</h6>
          <h6>• Buatlah bedengan persegi panjang</h6>
          <h6>• Lebar lahan yang mau di buat minimal 30-40cm</h6>
          <h6>• Tinggi kedalaman untuk penampungan air 20-30cm dan panjang bedengan sesuai dengan 
          lahan masing-masing
          </h6>
          <h6>• Bedengan di gembur dengan cangkul hingga merata dan halus bersihkan semua rumput-rumput liar di sekitar bedengan.
          </h6>
          <h5>2. Proses Pemupukan Awal</h5>
          <p>Setelah proses pengolahan lahan selesai berikanlah pupuk kandang sebelum biji atau tunas di 
          tanam dengan jarak 4 hari, pengalaman saya dalam meberi pupuk kandang pada lahan 8×12 
          saya beri 4 kantong dengan berat 50,kg, sebenernya tanaman kangkung darat cukup di bilang 
          sangat kebal terhadap cuaca dan kondisi tanah yang lembab.</p>
          <p>Proses pemberian pupuk pada saat usia umur tanaman kangkung darat berkisar 14 hari. 
          cukup di beri pupuk garam dan urea sekali sampai proses pemanenan juga tidak 
          bermasalah, selain meringankan pengeluaran juga bisa di bilang ngirit.</p>
          <h5>3. Cara Penanaman</h5>
          <h6>• Ada 2 tehnik penanaman kangkung darat yang bisa teman-teman lakukan yaitu dengan cara biji di tebar dan di tugal</h6>
          <h6>• Kalo proses penanaman bibit kangkung darat dengan cara di tebar maka hasilnya kurang 
          maksimal (bagus) nanti hasil pertumbuhanyapun akan sangat rapat dan tidak teratur</h6>
          <h6>• Jadi sebaiknya dalam proses penanaman kangkung darat alangkah baiknya dengan cara di 
          tugal saja dengan jarak tanam maxcimal 5-6cm, walaupun dengan cara di tugal proses 
          penanamannya agak lama tapi hasilnya akan sangat bagus dan rajin nantinya</h6>
          <h5>4. Penjarangan dan Penyulaman</h5>
          <p>Bila tanaman kangkung darat terlalu lebat maka akan sangat keliatan tidak rajin, jadi dalam 
          menanam tanaman kangkung darat biar menghasilkan hasil yang bagus maka diperlukan 
          penjarangan. Apabila tanaman banyak yang mati, Sebaiknya segeralah dilakukan penyulaman 
          dan diganti dengan bibit yang telah disiapkan sebelumnya begitupula sebaliknya, apabila di 
          dalam satau rumpun tanaman kebanyakan maka segeralah lakukan pemisahan minimal dalam 
          satu rumpun ada 3-4 batang kangkung saja.</p>
          <h5>Kacang Panjang</h5>
          <p>Kacang panjang (Vigna sinensis L) merupakan jenis sayuran yang populer di kalangan 
          masyarakat. Selain rasanya enak sayuran ini juga mengandung zat gizi bervariasi dan sangat 
          baik untuk kesehatan . selain itu juga dapat menyuburkan tanah sehingga dapat dikatakan 
          dwifungsi. Dengan memperhatikan luas tanam yang terus meningkat dan konsumsi kacang 
          panjang oleh masyarakat indonesia maka hal ini cukup menjadi peluang serta alasan untuk
          membudidayakan kacang panjang.</p>
          <p>Syarat tumbuh tumbuh kacang panjang antara lain : Ketinggian tempat : 0 – 1500 mdpl,Suhu 
          udara 18 – 32o C, optimum 25o C, Curah Hujan 600 – 2000 mm/tahun, Tekstur tanah liat 
          berpasir, pH tanah 5,5 – 6,5 dengan Persiapan benih, benih unggul bermutu dan kebutuhan 
          benih 15 – 20 kg/ ha.</p>
          <p>Pengolahan tanah : tanah dicangkul sedalam 20 – 30 cm dan dibiarkan selama 3 – 4 
          hari, kapur diberikan apabila pH < 5 pada saat pengolahan tanah, pembuatan bedengan 
          dengan ukuran lebar : 80 – 110 cm. Diantara bedengan dibuat saluran drainase dengan lebar 
          30 cm dan panjang sesuai keadaan lahan, di atas bedengan ditabur pupuk kandang diaduk 
          dengan tanah, bedengan ditutup dengan mulsa plastik hitam perak yang sudah diberi lobang 
          tanam dengan jarak yang dikehendaki.</p>
          <p>Penanaman kacang panjang dilakukan dengan cara ditugal sedalam 4-5 cm jarak antar 
          lobang tanam 25 – 30 cm, jarak antar barisan 60 -75 cm, setiap lobang tanam dimasukkan 2 
          butir, pemasangan turus.,turus dibuat dari batang kayu atau belahan bambu turus dengan 
          ukuran panjang 150 – 200 cm, lebar 2- 3 cm, pemasangan turus pada saat tanaman berumur 2 
          minggu atau tinggi tanaman mencapai 25 cm.</p>
          <p>Pemeliharaan kacang panjang diantaranya aadalah : Penyulaman dilakukan paling lambat 
          seminggu setelah tanam dengan benih yang telah disiapkan sebelumnya dalam polybag, 
          penyiraman dengan gembor. Untuk lahan yang cukup luas dilakukan dengan mengalirkan air 
          pada saluran disekitar bedengan, penyiangan Dilakukan dua kali yaitu setelah tanaman 
          berumur 3 minggu dan 6 minggu, Bersamaan dengan penyiangan dilakukan pendangiran</p>
          <p>Pemupukan dengan menggunakan Pupuk organik diberikan sebagai pupuk dasar. Pupuk urea, 
          SP- 36 dan KCL diberikan sebagai pupuk susulan dengan dosis perhektar: 100 kg urea, 200 
          kg, SP- 36 dan 100 kg KCL, urea diberikan setengah dosis pada saat tanam dan setengahnya 
          lagi pada saat tanaman berumur 3 minggu sedangkan SP-36 dan KCL diberikan seluruhnya 
          pada saat tanam, pupuk dimasukkan dalam lubang atau larikan sejauh 5 – 7 cm dari barisan 
          benih kemudian ditutup dengan tanah, Pemupukan juga dilakukan melalui daun sebagai 
          pelengkap</p>
          <p>Pemangkasan Dilakukan apabila tanaman terlalu subur atau terlalu banyak cabang yang 
          kuran produktif, pemangkasan dilakukan dengan cara memotong pucuk sekitar 2 – 3 ruas 
          unuk merangsang tumbuhnya cabang yang produktif</p>
          <p>OPT dan Pengendaliannya hama dan penyakit yang sering datang adalah : Hama : Kutu 
          kebul, ulat jengkal, lalat kacang, tungau merah, penggerek polong , kepik polong, 
          nematoda.Penyakit : Layu skleretium, karat daun, layu fusarium, bercak daun, penyakit sapu, 
          Mozaik, antraknosa.Pengendalian Lakukan pengamatan OPT secara intensif dan adakan 
          tindakan sedini mungkin jika ditemukan ada gejala serangan, Jaga kebersihan kebun untuk 
          mengantisipasi berkembang biaknya OPT, Musnahkan bagian tanaman yang sudah terserang 
          OPT, Pengendalian secara biologi dilakukan dengan musuh alami, Penyemprotan insektisida 
          merupakan langkah terakhir.</p>
