          <h3>Tanaman Kangkung Dan Kacang Panjang</h3>
          <br>
          <h4>Pemanenan</h4>
          <br>
          <h5>Kangkung</h5>
          <p>Pemanenan pada tanaman kangkung dapat dilakukan apabila kangkung sudah mencapai usia 
          30 sampai 40 hari. Ciri-ciri tanaman kangkung siap panen yaitu batangnya telah memanjang 
          sekitar 20-25 cm dan daunnya cukup besar. Waktu panen yang baik adalah saat pagi atau sore 
          hari. Pemanenan bisa dilakukan dengan dua cara yaitu dicabut atau dipotong paling bawah 
          batangnya.</p>
          <p>Disarankan untuk memanen kangkung dengan cara memotong paling bawah batangnya 
          karena batang pohon nantinya akan bisa tumbuh lagi dan bisa dibudidayakan ulang serta bisa 
          mengurangi pengeluaran untuk membeli bibit. Perlu diketahui bahwa kangkung darat bisa 
          dipanen sebanyak 3 kali dalam sekali tanam. Setelah panen pertama selesai, kangkung hanya 
          perlu 5 sampai 7 hari saja untuk bisa memetik kembali kangkung-kangkungnya.</p>
          <p>Apabila kangkung dipanen dengan cara dipotong paling bawah batangnya , maka kangkung 
          tersebut bisa panen sampai 6 kali dalam sebulan. Sedangkan jika memanen tanaman 
          kangkung dengan cara cabut sampai akarnya, maka hanya bisa sekali panen saja. Banyaknya 
          kangkung yang dihasilkan tergantung pada luas lahan dan berhasil tidaknya budidaya 
          tanaman kangkung. Biasanya dalam 1 hektar lahan bisa menghasilkan kangkung sebanyak 23 
          ton.</p>
          <br>
          <h6>Pasca Panen</h6>
          <p>Dalam budidaya kangkung, biasanya proses pemasaran kangkung dalam bentuk 
          sayuran segar. Cara yang dapat kamu lakukan selepas kangkung panen adalah :</p>
          <p>• Pengumpulan</p>
          <p>• Perlu menampung atau mengumpulkan ujung-ujung batang kangkung di tempat yang 
          strategis dan teduh</p>
          <p>• Pencucian</p>
          <p>Pada tempat pengumpulan, kangkung perlu segera dilakukan pencucian menggunakan air 
          mengalir atau bisa menyemprotkan airnya.</p>
          <p>• Sortasi dan Seleksi</p>
          <p>Apabila kangkung sudah bersih dicuci, lakukanlah pemilihan (sortasi) dan seleksi yaitu 
          dengan memisahkan ujung-ujung batang kangkung yang daunnya rusak atau busuk dan 
          memilh hasil panen yang terbaik.</p>
          <p>• Pengikatan dan Pengemasan</p>
          <p>• Dalam mengikat kangkung yang telah dipilih sebelumnya dengan tali rafia atau tali 
          plastik lainnya. Setiap ikatan, berbobot 20 sampai 25 kg atau sesuaikan dengan sarana 
          angkutan yang tersedia.</p>
          <p>• Penyimpanan</p>
          <p>Sebelum kangkung dipasarkan, sebaiknya simpan kangkung dalam ruangan dingin dan teduh. 
          bisa menyimpannya dalam lemari es atau tempat dengan remukan es. Hal ini dilakukan agar 
          kesegaran kangkung terjaga.</p>
          <br>
          <h5>Kacang Panjang</h5>
          <p>Seperti sifat sayuran pada umumnya, kacang panjang merupakan komoditi yang tidak tahan 
          lama, mudah busuk dan cepat menurun kualitasnya. Untuk melindungi hasil panen dari 
          kerusakan dan mempertahankan kualitas produk agar dapat diterima konsumen dalam 
          keadaan segar maka perlu dilakukan penanganan panen dan pasca panen. Penanganan panen 
          dan pascapanen sebaiknya dilakukan secara cermat dan hati-hati agar diperoleh hasil yang 
          baik.</p>
          <br>
          <h5>A. Panen</h5>
          <p>Perlakuan panen akan mempengaruhi hasil serta proses penanganan selanjutnya. Penanganan 
          panen yang baik akan memberikan kualitas produksi yang baik pula. Dalam pemanenan ada 
          beberapa hal yang harus diperhatikan, antara lain usia panen dan cara panen.</p>
          <br>
          <h6>1. Ciri dan Umur panen</h6>
          <p>Panen kacang panjang dibedakan 2 macam, yaitu panen polong muda dan panen polong tua 
          ataubiji-bijinya.
          Panen Polong Muda. Pada umumnya pemanenan polong muda kacang panjang pertama kali 
          dapat dilakukan setelah berumur 45 hari. Umur panen ini tergantung pada varietas, musim 
          dan tinggi rendahnya daerah penanaman. Polong muda sudah dapat dipanen sesudah terisi 
          penuh dan warna polongnya hijau merata sampai hijau keputihan. Polong yang muda mudah 
          dipatahkan. Semakin tua polong akan semakin liat, berserat, dan warnanya menguning. Oleh 
          karena itu, pemanenan sebaiknya tidak sampai terlambat. Polong yang terlambat dipanen 
          kurang baik untuk disayur dan tidak dapat dipasarkan.</p>
          <p>Pemanenan umumnya dilakukan pada pagi hari dan setelah dipanen biasanya kacang panjang 
          langsung dipasarkan pada siang atau sore harinya. Untuk mendapatkan kacang panjang segar 
          yang berkualitas baik, polong harus dipanen dengan selang waktu tiga hari sekali. Setelah 
          tanaman berumur sekitar 3-3,5 bulan pemanenan dihentikan, pada saat itu biasanya buahnya 
          sudah habis.</p>
          <p>Panen Polong Tua. Dilakukan untuk kacang panjang tipe tegak seperti kacang tunggak dan 
          kacang uci. Ciri-ciri kacang siap dipanen adalah polongnya telah cukup tua, biji-biji menonjol 
          dan kulit luar berwarna hijau kekuningan. Umur panen 3-3,5 bulan dan waktu panen pada 
          pagi atau sore hari.</p>
          <br>
          <h6>2. Cara Panen</h6>
          <p>Pemanenan kacang panjang dilakukan dengan cara memetik yaitu dengan memutar bagian 
          pangkal polong agar polong terlepas seluruhnya dan tidak menimbulkan luka yang besar. 
          Panen dengan memutar hingga seluruh polong terlepas dari tangkainya dapat merangsang 
          pembentukan buah baru lebih cepat. Cara panen dengan memetik di bagian pangkal polong 
          dapat meningkatkan hasil sampai 25%. Apabila panen dilakukan dengan menyisakan sedikit 
          polong pada tiap tangkai akan menghasilkan 2-4 buah. Namun bila seluruh polong dipetik, 
          tanaman mampu menghasilkan 4-6 polong tiap tangkai.</p>
          <p>Biasanya pemetikan polong dapat dilakukan 5-15 kali, sampai polongnya habis semua. Untuk 
          kacang panjang tipe tegak, pemanenan dengan cara mencabut atau memotong pangkal batang 
          tanaman setinggi 10-15 cm dari permukaan tanah. Selepas panen, polong kacang panjang 
          dikumpulkan ditempat penampungan, dicuci dan ditiriskan sedangkan untuk kacang polong 
          tua, setelah dikumpulkan, dikeringkan dengan dijemur.</p>
          <br>
          <h5>B. Penanganan Pasca Panen</h5>
          <p>Penanganan pasca panen kacang panjang antara lain sortasi, pengemasan dan pengangkutan.</p>
          <br>
          <h6>1. Sortasi</h6>
          <p>Tidak semua kacang yang telah dipanen layak dipasarkan, sehingga perlu disortasi atau 
          pemilahan berdasarkan kualitas dan keseragaman, Polong kacang panjang dipilah antara yang 
          baik dan yang cacat. Polong yang cacat berbintik hitam, berlubang, atau busuk disisihkan. 
          Ukuran sasaran ekspor, kriteria ketuaan paling muda yaitu ukuran polong sesuai permintaan 
          pasar, tingkat ketuaan polong tergolong muda, penampilan biji tidak menonjol dan berwarna 
          hijau dan segar. Sedangkan polong tua yang sudah kering dipisahkan dari kulit polong dan 
          biji dikeringkan sampai 12%-14% kadar airnya. Untuk pasar tradisional, sortasi kurang 
          diperhatikan karena pasar tradisional tidak menuntut keseragaman kualitas yang baik 
          sedangkan pasar swalayan yang menghendaki produk yang seragam dan baik kualitasnya.</p>
          <br>
          <h6>2. Pengemasan</h6>
          <p>Pengemasan bertujuan untuk memudahkan dalam pengangkutan. Kacang panjang yang akan
          dipasarkan di pasar tradisional biasanya tidak dikemas secara khusus. Pengemasan dilakukan 
          sederhana dalam bentuk ikatan besar atau kecil yang beratnya sekitar 250-2.000 gram tiap 
          ikat. Ikatan kacang panjang biasanya menggunakan tali rafia atau tali bambu. Ikatan dikemas 
          dalam karung goni yang berventilasi atau dalam kantong plastik polyelene. Setelah diikat, 
          sayuran langsung disusun pada kendaraan pengangkut. Penyusunan diatur agar tumpukan 
          tidak terlalu padat karena akan merusak sayuran. Apabila kacang panjang akan dipasarkan 
          untuk jarak jauh, sebaiknya dikemas dalam peti, keranjang bambu atau plastik agar tidak 
          cepat rusak.</p>
          <p>Untuk konsumsi pasar swalayan, kacang panjang juga dikemas sederhana. Setiap kelompok 
          diikat dengan plastik isolasi dan diberi label. Ikatan-ikatan tersebut dimasukkan ke dalam 
          keranjang plastik, selanjutnya siap dipasarkan.</p>
          <p>Untuk polong tua dikemas dalam kaleng yang tertutup rapat, sebelumnya dimasukkan ke 
          dalam wadah sebaiknya dicampur dulu dengan minyak jagung supaya terlindung dari hama 
          penggerek.</p>
          <br>
          <h6>3. Pengangkutan</h6>
          <p>Untuk pemasaran jarak jauh diusahakan sayuran terlindung dari sinar matahari dan hujan agar 
          tidak cepat layu atau busuk. Sebaiknya kacang panjang yang selesai dipanen langsung 
          diangkut agar diterima konsumen dalam keadaan masih segar.</p>
